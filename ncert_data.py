def get_hindi9_context(topic: str) -> str:
    """
    Build a chapter-reference block to inject into the generation prompt
    for Class 9 Hindi requests. Includes actual textbook text where available.
    """
    lines = [
        "=== NCERT Class 9 Hindi — Chapter Reference (Actual Textbook Content) ===",
        "Identify which chapter the teacher is asking about from the topic string below.",
        "Use ONLY the actual text, meanings, and key points listed for that chapter.",
        "CRITICAL: Do NOT use any famous dohas/savaiye/poems by these authors unless they appear verbatim below.",
        f"Teacher topic: \"{topic}\"",
        "",
        "--- KSHITIJ (क्षितिज) ---",
    ]

    for ch in HINDI_9["kshitij"]:
        type_label = "गद्य" if ch["type"] == "gadya" else "काव्य"
        lines.append(f"\nCh {ch['no']}: {ch['title']} — {ch['author']} [{type_label}]")
        lines.append(f"Summary: {ch['summary'][:200]}")
        lines.append(f"Themes: {'; '.join(ch['themes'])}")

        if "actual_text" in ch:
            at = ch["actual_text"]
            lines.append("ACTUAL TEXTBOOK TEXT:")

            if "sakhiyan" in at:
                lines.append("  साखियाँ (exactly 7 dohas in NCERT — use only these):")
                for s in at["sakhiyan"]:
                    lines.append(f"  साखी {s['no']}:\n    {s['doha']}")
                    lines.append(f"    अर्थ: {s['meaning']}")
                    lines.append(f"    Key: {s['key_point']}")
            if "sabads" in at:
                lines.append("  सबद (exactly 2 sabads in NCERT — use only these):")
                for s in at["sabads"]:
                    lines.append(f"  सबद {s['no']} — {s['title']}:\n    {s['text']}")
                    lines.append(f"    अर्थ: {s['meaning']}")
                    lines.append(f"    Key: {s['key_point']}")
            if "vakhs" in at:
                lines.append("  वाख (use only these):")
                for v in at["vakhs"]:
                    lines.append(f"  वाख {v['no']}:\n    {v['text']}")
                    lines.append(f"    Key: {v['key_point']}")
            if "savaiye" in at:
                lines.append("  सवैये (exactly 4 in NCERT — use only these):")
                for s in at["savaiye"]:
                    lines.append(f"  सवैया {s['no']}:\n    {s['text']}")
                    lines.append(f"    अर्थ: {s['meaning']}")
                    lines.append(f"    Key: {s['key_point']}")
            if "key_stanzas" in at:
                lines.append("  Key stanzas:")
                for ks in at["key_stanzas"]:
                    lines.append(f"    {ks}")
                if "notes" in at:
                    lines.append(f"  Notes: {at['notes']}")
            if "opening_stanza" in at:
                lines.append(f"  Opening stanza:\n    {at['opening_stanza']}")
            if "opening_line" in at:
                lines.append(f"  Opening line: {at['opening_line']}")
                if "notes" in at:
                    lines.append(f"  Notes: {at['notes']}")
            if "full_poem" in at:
                lines.append(f"  Full poem:\n    {at['full_poem']}")
                if "notes" in at:
                    lines.append(f"  Notes: {at['notes']}")
        else:
            key_els = ch.get("key_elements", [])
            if key_els:
                lines.append(f"Key elements: {'; '.join(key_els[:3])}")

        lines.append(f"NCERT questions: {'; '.join(ch['ncert_focus'][:2])}")

    lines.append("\n--- KRITIKA (कृतिका) ---")
    for ch in HINDI_9["kritika"]:
        lines.append(f"\nCh {ch['no']}: {ch['title']} — {ch['author']}")
        lines.append(f"Summary: {ch['summary']}")

    lines.append("\n=== END Chapter Reference ===")
    lines.append(
        "FINAL REMINDER: Reference ONLY the dohas/vakhs/savaiye listed above. "
        "Never substitute with other poems by the same author from outside this textbook."
    )
    return "\n".join(lines)
